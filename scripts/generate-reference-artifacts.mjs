import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const rulesPath = resolve(process.env.MACHINE_RULES_PATH ?? resolve(root, "skill/clean-ui-design/references/machine-rules.json"));
const manifestPath = resolve(root, "skill/clean-ui-design/references/manifest.json");
const generatedPath = resolve(root, "src/references/generated.ts");
const check = process.argv.slice(2).join(" ") === "--check";

const expectedKeys = ["schemaVersion", "bannedPhrases", "vaguePhrases", "patterns", "exemptionPatterns"];
const patternKeys = ["unlabeledControlRisk", "pixelMeasurement"];
const exemptionKeys = ["domGroundTruth", "contrastClause", "positiveLabelPairing"];

function fail(message) {
  throw new Error(`Invalid machine rules: ${message}`);
}

function object(value, path) {
  if (typeof value !== "object" || value === null || Array.isArray(value)) fail(`${path} must be an object`);
  return value;
}

function exactKeys(value, keys, path) {
  const actual = Object.keys(object(value, path)).sort();
  const expected = [...keys].sort();
  if (actual.length !== expected.length || actual.some((key, index) => key !== expected[index])) {
    fail(`${path} must contain exactly: ${expected.join(", ")}`);
  }
}

function phrases(value, path) {
  if (!Array.isArray(value) || value.length === 0 || value.some((phrase) => typeof phrase !== "string" || phrase.trim() === "")) {
    fail(`${path} must be a non-empty array of strings`);
  }
  if (new Set(value.map((phrase) => phrase.toLowerCase())).size !== value.length) fail(`${path} must not contain duplicates`);
  return value;
}

function pattern(value, path) {
  exactKeys(value, ["source", "flags"], path);
  if (typeof value.source !== "string" || typeof value.flags !== "string") fail(`${path} requires string source and flags`);
  try { new RegExp(value.source, value.flags); } catch (error) { fail(`${path} does not compile: ${error.message}`); }
  return value;
}

function loadRules() {
  let rules;
  try { rules = JSON.parse(readFileSync(rulesPath, "utf8")); } catch (error) { fail(`JSON parse failed: ${error.message}`); }
  exactKeys(rules, expectedKeys, "root");
  if (rules.schemaVersion !== 1) fail("schemaVersion must be 1");
  phrases(rules.bannedPhrases, "bannedPhrases");
  phrases(rules.vaguePhrases, "vaguePhrases");
  exactKeys(rules.patterns, patternKeys, "patterns");
  exactKeys(rules.exemptionPatterns, exemptionKeys, "exemptionPatterns");
  for (const key of patternKeys) pattern(rules.patterns[key], `patterns.${key}`);
  for (const key of exemptionKeys) pattern(rules.exemptionPatterns[key], `exemptionPatterns.${key}`);
  return rules;
}

function loadReferenceMetadata() {
  let manifest;
  try { manifest = JSON.parse(readFileSync(manifestPath, "utf8")); } catch (error) { throw new Error(`Invalid reference manifest: ${error.message}`); }
  if (!Array.isArray(manifest)) throw new Error("Invalid reference manifest: expected an array");
  return manifest.map((entry) => {
    if (!entry || typeof entry !== "object" || typeof entry.id !== "string" || !Number.isInteger(entry.version) || !Array.isArray(entry.purposes)) {
      throw new Error("Invalid reference manifest: every entry needs id, version, and purposes");
    }
    return { id: entry.id, version: entry.version, purposes: entry.purposes };
  });
}

function literal(value) {
  return JSON.stringify(value, null, 2);
}

function regexExpression({ source, flags }) {
  return `new RegExp(${JSON.stringify(source)}, ${JSON.stringify(flags)})`;
}

function render(rules, references) {
  const exemptions = Object.entries(rules.exemptionPatterns)
    .map(([name, definition]) => `  ${name}: ${regexExpression(definition)},`)
    .join("\n");
  return `// This file is generated by scripts/generate-reference-artifacts.mjs. Do not edit.\n\n` +
    `export const BANNED_PHRASES = Object.freeze(${literal(rules.bannedPhrases)}) as readonly string[];\n\n` +
    `export const VAGUE_PHRASES = Object.freeze(${literal(rules.vaguePhrases)}) as readonly string[];\n\n` +
    `export const UNLABELED_CONTROL_RISK = ${regexExpression(rules.patterns.unlabeledControlRisk)};\n` +
    `export const PIXEL_MEASUREMENT = ${regexExpression(rules.patterns.pixelMeasurement)};\n\n` +
    `export const EXEMPTION_PATTERNS = Object.freeze({\n${exemptions}\n});\n` +
    `\nexport const REFERENCE_METADATA = Object.freeze(${literal(references)}) as readonly { id: string; version: number; purposes: readonly string[] }[];\n`;
}

const output = render(loadRules(), loadReferenceMetadata());
if (check) {
  const current = readFileSync(generatedPath, "utf8");
  if (current !== output) {
    console.error("Generated reference artifact is stale. Run: npm run generate-references");
    process.exitCode = 1;
  }
} else {
  writeFileSync(generatedPath, output);
}
